function hasBatchim(text: string): boolean {
  const last = text[text.length - 1];
  const code = last?.charCodeAt(0) ?? 0;
  if (code < 0xac00 || code > 0xd7a3) return false;
  return (code - 0xac00) % 28 !== 0;
}

