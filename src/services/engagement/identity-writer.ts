function resolveWriterFrame(mode: string, focus: WriterFocus, seed: number): KoWriterFrame {
  if (mode === "interaction-experiment") return "quest";
  if (mode === "meta-reflection") return "cross-exam";
  if (mode === "identity-journal" && focus === "retention") return "field-note";
  if (mode === "identity-journal" && focus === "hype") return "claim-note";
  if (mode === "meta-reflection" && focus === "execution") return "cross-exam";
  if (mode === "philosophy-note" && focus === "liquidity") return "claim-note";
  if (mode === "identity-journal") return seed % 2 === 0 ? "field-note" : "claim-note";
  if (mode === "philosophy-note") return seed % 2 === 0 ? "claim-note" : "cross-exam";
  return ["claim-note", "field-note", "cross-exam"][(seed % 3)] as KoWriterFrame;
}

function hasSimilarCadence(left: string, right: string): boolean {
  const normalize = (text: string) => sanitizeClause(text).replace(/\s+/g, " ");
  const a = normalize(left);
  const b = normalize(right);
  if (!a || !b) return false;
  const aTail = a.split(" ").slice(-1)[0];
  const bTail = b.split(" ").slice(-1)[0];
  if (aTail && aTail === bTail) return true;
  return /(경계한다|의심한다|믿지 않는다|힘을 잃는다|오래 못 간다|가깝다|멈춘다)$/.test(a) && /(경계한다|의심한다|믿지 않는다|힘을 잃는다|오래 못 간다|가깝다|멈춘다)$/.test(b) && aTail === bTail;
}

function hasHeavyKeywordOverlap(left: string, right: string): boolean {
  const keywords = ["서사", "사용", "재방문", "광고", "사람", "집행", "기사", "규제", "체결", "돈", "복구", "열기"];
  const a = sanitizeClause(left);
  const b = sanitizeClause(right);
  const overlap = keywords.filter((keyword) => a.includes(keyword) && b.includes(keyword));
  return overlap.length >= 2;
}

function pickDistinctLine(
  pool: string[],
  seed: number,
  variant: number,
  blockers: string[],
  extraSalt = 0
): string {
  const effectivePool = pool.filter(Boolean);
  if (!effectivePool.length) return "";
  const baseIndex = Math.abs(seed + extraSalt) % effectivePool.length;
  for (let offset = 0; offset < effectivePool.length; offset += 1) {
    const candidate = effectivePool[(baseIndex + variant + offset) % effectivePool.length];
    const blocked = blockers.some(
      (blocker) =>
        blocker &&
        (hasSimilarCadence(candidate, blocker) || hasHeavyKeywordOverlap(candidate, blocker))
    );
    if (!blocked) return candidate;
  }
  return effectivePool[(baseIndex + variant) % effectivePool.length];
}

function resolveWriterFocus(input: KoIdentityWriterInput, primaryAnchor: string, secondaryAnchor: string): WriterFocus {
  if (input.preferredFocus && input.preferredFocus !== "general") {
    return input.preferredFocus;
  }
  const merged = sanitizeTweetText(
    [input.headline, primaryAnchor, secondaryAnchor, input.worldviewHint, input.signatureBelief, input.recentReflection]
      .filter(Boolean)
      .join(" ")
      .toLowerCase()
  );
  const sceneWeighted = sanitizeTweetText([input.headline, secondaryAnchor, primaryAnchor].filter(Boolean).join(" ").toLowerCase());

  if (input.lane === "ecosystem") {
    if (/(개발자|빌드|코드|예치 자금|잠긴 자금|tvl)/.test(sceneWeighted)) return "builder";
    const retentionHits =
      (sceneWeighted.match(/재방문|돌아오|머무|잔류/g) || []).length * 2 +
      (sceneWeighted.match(/사용자|이용자|유저|실사용/g) || []).length +
      (merged.match(/재방문|돌아오|머무|잔류|사용자|이용자|유저|실사용/g) || []).length;
    const hypeHits =
      (sanitizeTweetText([input.headline, primaryAnchor].filter(Boolean).join(" ").toLowerCase()).match(/서사|홍보|광고|커뮤니티|열기|캠페인|포스터/g) || []).length * 2 +
      (merged.match(/서사|홍보|광고|커뮤니티|열기|캠페인|말만\s*커지|포스터/g) || []).length;
    if (hypeHits > retentionHits) return "hype";
    if (retentionHits > 0) return "retention";
    if (hypeHits > 0) return "hype";
  }
  if (input.lane === "regulation") {
    if (/(법원|소송|판결|court|lawsuit)/.test(merged)) return "court";
    if (/(집행|현장|행동|기사|정책|규제|논평|해설)/.test(merged)) return "execution";
  }
  if (input.lane === "market-structure") {
    if (/(호가 유동성|현물 체결|깊이|depth|settlement)/.test(merged)) return "settlement";
    if (/(호가|체결|주문|자금|과열|유동성|돈)/.test(merged)) return "liquidity";
  }
  if (input.lane === "onchain") {
    if (/(고래|거래소 자금|자금 방향|whale|exchange)/.test(merged)) return "flow";
    if (/(지속|하루|버티|숫자|신호|흔적)/.test(merged)) return "durability";
  }
  if (input.lane === "protocol") {
    if (/(메인넷|launch|준비도|복귀 자금|예치 자금)/.test(merged)) return "launch";
    if (/(복구|운영|검증자|합의|업그레이드|release|recovery)/.test(merged)) return "durability";
  }
  return "general";
}

